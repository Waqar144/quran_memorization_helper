void main() {
  test("test PageForAyah", () {
    for (final mushaf in Mushaf.values) {
      final data = switch (mushaf) {
        Mushaf.Indopak16Line => data16Line,
        Mushaf.Uthmani15Line => data15Uthmani,
        Mushaf.Indopak15Line => data15Indo,
      };

      for (int i = 0; i < 6236; i += 50) {
        int x = getParaPageForAyah(i, mushaf);
        expect(x, data[i]);
      }

      for (int i = 6220; i < 6236; i += 1) {
        int x = getParaPageForAyah(i, mushaf);
        expect(x, data[i]);
      }
    }

    print("test 'Page For Ayah' OK");
  });

  test("test PageForAyah returns valid", () {
    for (final mushaf in Mushaf.values) {
      for (int i = 0; i < 6236; i += 1) {
        int x = getParaPageForAyah(i, mushaf);
        if (x < 0) {
          print("$i -> $x --- $mushaf");
        }
        expect(x >= 0, true);
      }
    }

    print("test 'PageForAyah returns valid' OK");
  });
}

