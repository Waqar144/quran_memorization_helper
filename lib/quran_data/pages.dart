int pageCountForPara(int paraIdx) {
  if (paraIdx == 29) {
    return 22;
  }
  int p1 = para16LinePageOffsets[paraIdx];
  int p2 = para16LinePageOffsets[paraIdx + 1];
  return p2 - p1;
}

int paraForPage(int page) {
  if (page < 0 || page > 548) {
    throw "Invalid page number: $page";
  }

  for (int i = 0; i < 30; ++i) {
    if (page >= para16LinePageOffsets[i]) {
      continue;
    }
    return i - 1;
  }
  return 30 - 1;
}

