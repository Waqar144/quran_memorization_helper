class AyahOffset {
  final int start;
  final int? len;
  const AyahOffset(this.start, this.len);
}

AyahOffset getAyahRange(int ayah) {
  if (ayah > _ayahOffsets.length) {
    throw "Invalid ayah $ayah";
  }
  int s = _ayahOffsets[ayah];
  int e = ayah + 1 >= _ayahOffsets.length ? -1 : _ayahOffsets[ayah + 1];
  int? len = e == -1 ? null : (e - s);
  return AyahOffset(s, len);
}

Ayat getAyahForIdx(int ayahIdx, final ByteBuffer quranText,
    {List<int> markedWords = const []}) {
  final AyahOffset off = getAyahRange(ayahIdx);
  final text = utf8.decode(quranText.asUint8List(off.start, off.len));
  return Ayat(text.trim(), markedWords, ayahIdx: ayahIdx);
}

