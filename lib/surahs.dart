int toSurahAyahOffset(int surahIdx, int absoluteAyah) {
  if (surahIdx > _surahAyahOffsets.length) {
    throw "Invalid surah $surahIdx";
  }
  return absoluteAyah - _surahAyahOffsets[surahIdx];
}

